package org.tnsif.FinalKeyword;

public class FinalVarible {
	//final varible must be initialized at declaration.
	//final int x;
	final float Salary=67000.0f;
	void print()
	{ //we cant change the final variable
		//Salary=78000.00f;
		System.out.println("Salary"+" "+Salary);
	}
	

}
